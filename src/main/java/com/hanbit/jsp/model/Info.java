package com.hanbit.jsp.model;

public class Info {

	private String name;
	private int countOfButtons;
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getCountOfButtons() {
		return countOfButtons;
	}
	
	public void setCountOfButtons(int countOfButtons) {
		this.countOfButtons = countOfButtons;
	}
	
}
